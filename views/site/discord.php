<h2>Instruction On How To Use</h2>
<ol style="font-size:18px;">
    <li>As an Administrator or channel manager you need to type 2 commands to set up the bot.</li>
    <li>type <pre>/ahsetrealm < realm_name ></pre></li> This realm_name is the name of realm.
    <li>then type <pre>/ahsetfaction < faction ></pre> faction can be <b>a</b>(alliance) or <b>h</b>(horde)</li>
    <li>then you are all set! <pre>/ahsearch < item name ></pre> item name is any item that you are looking for eg : pillars of might, cardinal ruby</li>
    <br>For more advanced settings, it's possible to adjust faction and/or realm per channel. The server default is overridden by this setting only in the channels it is configured in</br>
    <b>In the channel you want to configure:</b>
    <li>type <pre>/ahsetchannelrealm < realm_name ></pre> The realm name is the same as the server wide realm setting command</li>
    <li>type <pre>/ahsetchanneldfaction < faction ></pre> The faction is the same as the server wide faction setting command</li>
</ol>
